#include <stdio.h>

int main()
{
    int a = 54;
    int b = 38;

    // Sum of two numbers
    printf("The value of a is %d and the value of b is %d, and the sum of a and b is %d\n", a, b, a + b);

    // Subtract of two numbers
    printf("The Subtract of a and b is %d\n", a - b);

    // Multiplication of two numbers
    printf("The multiplication of a and b is %d\n", a * b);

    // Division of two numbers
    printf("The division of a and b is %d\n", a / b);

    // Modulus (This operator is used to get the remainder)
    printf("Remainder when a is divided by b is %d\n", a % b);

    return 0;
}